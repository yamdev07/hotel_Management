    private void DELETE_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DELETE_BTNActionPerformed
        // TODO add your handling code here:
        int myIndex = USERLIST_JTABLE.getSelectedRow();
        if (myIndex == -1) {
            JOptionPane.showMessageDialog(null, "Aucune line du tableau n'est selectionnÃ©");
        } else {
            DefaultTableModel model = (DefaultTableModel) USERLIST_JTABLE.getModel();
            if (UsersController.destroy(Integer.parseInt(model.getValueAt(myIndex, 0).toString()))) {
                USERLIST_JTABLE.setModel(UsersController.getAll());// pour rafraichir la table
            }
        }
    }//GEN-LAST:event_DELETE_BTNActionPerformed

    private void UPDATE_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UPDATE_BTNActionPerformed
        // TODO add your handling code here:
        //Controller.LoginController.update();
        
        int myIndex = USERLIST_JTABLE.getSelectedRow();
        if (myIndex == -1) {
            JOptionPane.showMessageDialog(null,"No table row is selected");
        } else {
            DefaultTableModel model = (DefaultTableModel) USERLIST_JTABLE.getModel();
            if (UsersController.update(Integer.parseInt(model.getValueAt(myIndex, 0).toString()), Name_TF.getText(), jLabel3.getText())) {
                USERLIST_JTABLE.setModel(UsersController.getAll());
                JOptionPane.showMessageDialog(null, "the modification is carried out successfully");
            }
        }
    }//GEN-LAST:event_UPDATE_BTNActionPerformed

