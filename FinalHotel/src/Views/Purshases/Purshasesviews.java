    private void SUPRIMER_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SUPRIMER_BTNActionPerformed
        // TODO add your handling code here:
          int myIndex = ACHATS_TABLE.getSelectedRow();
        if (myIndex == -1) {
            JOptionPane.showMessageDialog(null, " Aucune line du tableau n'est selectionné ");
        } else {
                    DefaultTableModel model = (DefaultTableModel) ACHATS_TABLE.getModel();
            if (PurshasesController.destroy(Integer.parseInt(model.getValueAt(myIndex, 0).toString()))) {
                ACHATS_TABLE.setModel(PurshasesController.getAll());// pour rafraichir la table
            }
        }
    }//GEN-LAST:event_SUPRIMER_BTNActionPerformed

    private void MODIFIER_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MODIFIER_BTNActionPerformed
        // TODO add your handling code here:
           int myIndex = ACHATS_TABLE.getSelectedRow();
        if (myIndex == -1) {
            JOptionPane.showMessageDialog(null," No table row is selected ");
        } else {
            DefaultTableModel model = (DefaultTableModel) ACHATS_TABLE.getModel();
            if (PurshasesController.update(Integer.parseInt(model.getValueAt(myIndex, 0).toString()), article_CB.getSelectedItem().toString(), client_CB.getSelectedItem().toString(), quantité_TF.getText(), paye_CB.getSelectedItem().toString())) {
                ACHATS_TABLE.setModel(PurshasesController.getAll());
                JOptionPane.showMessageDialog(null, "the modification is carried out successfully");
            }
        }
    }//GEN-LAST:event_MODIFIER_BTNActionPerformed

