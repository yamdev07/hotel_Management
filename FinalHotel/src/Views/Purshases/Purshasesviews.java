    private void article_CBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_article_CBMouseClicked
        // TODO add your handling code here:
        try {
            Manager.connect();
            String query = "SELECT * FROM articles";
            ResultSet resultSet = connection.createStatement().executeQuery(query);
            article_CB.removeAllItems();
            while (resultSet.next()) {
                article_CB.addItem(resultSet.getString("id"));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_article_CBMouseClicked

